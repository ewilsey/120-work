// Declare setup function
function setup() {

    // create a canvas
    createCanvas( 900, 600 );

    // color the background
    background( 'magenta' );
}


// Declare draw function
function draw() {

    // tell p5 to print the text string "Hello World!"
    // This is going to be positioned at ( x: 20, y: 30 ).
    text( "Hello World!", 10, 10 );
}
